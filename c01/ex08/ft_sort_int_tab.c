#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	swap;

	while (size >= 0)
	{
		a = 0;
		while (a < size - 1)
		{
			if (tab[a] > tab[a + 1])
			{
				swap = tab [a];
				tab[a] = tab [a + 1];
				tab [a + 1] = swap;
			}
			a++;
		}
		size--;
	}
}
