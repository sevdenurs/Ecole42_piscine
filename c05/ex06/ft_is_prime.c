#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	a;

	a = 2;
	if (nb <= 1)
		return (0);
	while (a <= (nb / 2))
	{
		if (!(nb % a))
			return (0);
		else
			a += 1;
	}
	return (1);
}
